class Category {
    constructor(id, name) {
        this.id = id;
        this.name = name;


    }

    getWidth() {
        return this.id;
    }

    setWidth(id) {
        this.id = id;
    }

    getHeight() {
        return this.name;
    }

    setHeight(name) {
        this.name = name;
    }


}



